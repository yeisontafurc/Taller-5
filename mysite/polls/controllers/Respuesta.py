

class Respuesta:
    def __init__(self, lista, titulo):
        self.lista = lista
        self.titulo = titulo

    def getLista(self):
        return  self.lista

    def getTitulo(self):
        return  self.titulo

    def setLista(self, lista):
        self.lista = lista

    def setTitulo(self, titulo):
        self.titulo = titulo

